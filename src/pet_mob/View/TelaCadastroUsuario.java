    public void Salvar() {

        this.mysql.conectaBanco();

        usuario.setNome(campoNome.getText());
        usuario.setSobrenome(campoSobrenome.getText());
        usuario.setCep(campoCep.getText());
        usuario.setSexo(campoSexo.getSelectedItem().toString());
        usuario.setIdade(campoIdade.getText());
        usuario.setCpf(campoCpf.getText());
        usuario.setEstado(campoEstado.getSelectedItem().toString());
        usuario.setCidade(campoCidade.getText());
        usuario.setRua(campoRua.getText());
        usuario.setNumeroCasa(campoNumeroCasa.getText());
        usuario.setComplemento(campoComplemento.getText());
        usuario.setEmail(campoEmail.getText());
        usuario.setTelefone(campoTelefone.getText());
        usuario.setCelular(campoCelular.getText());
        usuario.setSenha(campoSenhaUsuario.getText());
        usuario.setBairro(campoBairro.getText());
        usuario.setCep(campoCep.getText());
        try {

            this.mysql.insertSQL("insert into Usuario ("
                    + "cpf_user, "
                    + "nome_user, "
                    + "sobrenome_user ,"
                    + "sexo ,"
                    + "idade ,"
                    + "rua_usuario ,"
                    + "cidade_usuario ,"
                    + "bairro_usuario ,"
                    + "cep_usuario, "
                    + "email_usuario, "
                    + "telefone_usuario,"
                    + "estado_user,"
                    + "numero_casa, "
                    + "complemento_user,"
                    + "celular_user,"
                    + "senha_user"
                    + ") values ("
                    + "'" + usuario.getCpf() + "',"
                    + "'" + usuario.getNome() + "',"
                    + "'" + usuario.getSobrenome() + "',"
                    + "'" + usuario.getSexo() + "',"
                    + "'" + usuario.getIdade() + "',"
                    + "'" + usuario.getRua() + "',"
                    + "'" + usuario.getCidade() + "',"
                    + "'" + usuario.getBairro() + "',"
                    + "'" + usuario.getCep() + "',"
                    + "'" + usuario.getEmail() + "',"
                    + "'" + usuario.getTelefone() + "',"
                    + "'" + usuario.getEstado() + "',"
                    + "'" + usuario.getNumeroCasa() + "',"
                    + "'" + usuario.getComplemento() + "',"
                    + "'" + usuario.getCelular() + "',"
                    + "'" + usuario.getSenha() + "'"
                    + ");");

        } catch (Exception e) {

            System.out.println("Erro ao cadastrar cliente " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro no Cadastro");
        } finally {
            this.mysql.fechaBanco();
            JOptionPane.showMessageDialog(null, "Cliente Cadastrado com Sucesso");

        }

    }

