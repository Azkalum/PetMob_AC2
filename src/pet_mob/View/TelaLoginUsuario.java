    public boolean logar(Usuario usuario){
        this.conectar.conectaBanco();
        
        String usuarioLogin = this.campoUsuario.getText();
        String senha = this.campoSenha.getText();
        
        try{
            this.conectar.executarSQL(
                    "Select "
                    + "cpf_user, "
                    + "senha_user"
                    +" FROM "
                    +" Usuario"
                    +" WHERE "
                    + " cpf_user = '" + usuarioLogin + "'"
                    + " AND "
                    + " senha_user = '" + senha + "'"
                    +";"
            );
            while(this.conectar.getResultSet().next()){
                if(this.conectar.getResultSet().getString(1).equals(campoUsuario.getText()) && this.conectar.getResultSet().getString(2).equals(campoSenha.getText())){
                user.setCpf(campoUsuario.getText());
                return true;
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario e senha inv치lidos");
                    return false;
                }
            }
    }   catch (SQLException ex) {  
            Logger.getLogger(TelaLoginUsuario.class.getName()).log(Level.SEVERE, null, ex);
            
        }
        return false;
    }

